package com.Arrey;

public class CheckArrClass {
	public static void main(String[] args) {
		int i[]=new int[5];
		byte b[]=new byte[4];
		short c[]=new short[5];
		long l[]=new long[4];
		String s[]=new String[5];
		char ch[]=new char[5];
		float f[]=new float[5];
		double d[]=new double[5];
		boolean bl[]=new boolean[5];
		
		System.out.println("class type of int: "+i);
		System.out.println("class type of byte: "+b);
		System.out.println("class type of short: "+c);
		System.out.println("class type of long: "+l);
		System.out.println("class type of String: "+s);
		System.out.println("class type of char: "+ch);
		System.out.println("class type of float: "+f);
		System.out.println("class type of double: "+d);
		System.out.println("class type of boolean: "+bl);
	}
}
